#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	t_uchar	c1;
	t_uchar	c2;

	c1 = (t_uchar)*s1;
	c2 = (t_uchar)*s2;
	return (c1 && c2 && c1 == c2 ? ft_strcmp(s1 + 1, s2 + 1) : c1 - c2);
}
